package com.quest.practice1;

public class DoWhileEg {

    public static void main(String[] args) {
        int i = 1;


        do {
            System.out.println("Number: " + i);
            i++;
        } while (i <= 5);


        System.out.println();
        //while loop
        i=1;
        while (i <= 5) {
            System.out.println("Number: " + i);
            i++;
        }
    }
}

