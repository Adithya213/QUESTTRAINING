package com.quest.practice1;

public class DataTypes {

    public static void main(String[] args) {
        int myInt = 100;
        float myFloat = 10.5f;
        double myDouble = 20.99;
        char myChar = 'A';
        boolean myBool = true;
        byte myByte = 50;
        short myShort = 30000;
        long myLong = 123456789L;

        System.out.println("int eg: " + myInt);
        System.out.println("float eg: " + myFloat);
        System.out.println("double eg: " + myDouble);
        System.out.println("char eg: " + myChar);
        System.out.println("boolean eg: " + myBool);
        System.out.println("byte eg: " + myByte);
        System.out.println("short eg: " + myShort);
        System.out.println("long eg: " + myLong);
    }
}
